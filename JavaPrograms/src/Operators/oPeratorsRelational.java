package Operators;

public class oPeratorsRelational {

	public static void main(String[] args) {
		//create variables//
		int a = 11;
		int b = 11;
		
		 // value of a and b
		System.out.println("a is : " + a + "and b is : " + b);
		
		 // == operator
	    System.out.println(a == b);  // false

	    // != operator
	    System.out.println(a != b);  // true

	    // > operator
	    System.out.println(a > b);  // false

	    // < operator
	    System.out.println(a < b);  // true

	    // >= operator
	    System.out.println(a >= b);  // false

	    // <= operator
	    System.out.println(a <= b);  // true	 

	}

}
